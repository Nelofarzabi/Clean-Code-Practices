package chapter_02_meaningful_names.meaningful_context;

public class ContextualNamingExample {
    public static void main(String[] args) {
        String customerFullName = "Sarah Johnson";
        String customerBillingAddress = "123 Elm Street, Springfield";

        System.out.println("Customer Name: " + customerFullName);
        System.out.println("Billing Address: " + customerBillingAddress);
    }
}
